package org.example.qdunavigation.pojo;

public class Edge {
    public String edgeNo;
    public String srcNode;
    public String tarNode;
    public int length;

    public String getEdgeNo() {
        return edgeNo;
    }

    public void setEdgeNo(String edgeNo) {
        this.edgeNo = edgeNo;
    }

    public String getSrcNode() {
        return srcNode;
    }

    public void setSrcNode(String srcNode) {
        this.srcNode = srcNode;
    }

    public String getTarNode() {
        return tarNode;
    }

    public void setTarNode(String tarNode) {
        this.tarNode = tarNode;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
