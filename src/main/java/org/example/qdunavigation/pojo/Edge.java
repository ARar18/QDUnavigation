package org.example.qdunavigation.pojo;

public class Edge {
    public int edgeNo;
    public int srcNode;
    public int tarNode;
    public int length;

    public int getEdgeNo() {
        return edgeNo;
    }

    public void setEdgeNo(int edgeNo) {
        this.edgeNo = edgeNo;
    }

    public int getSrcNode() {
        return srcNode;
    }

    public void setSrcNode(int srcNode) {
        this.srcNode = srcNode;
    }

    public int getTarNode() {
        return tarNode;
    }

    public void setTarNode(int tarNode) {
        this.tarNode = tarNode;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
